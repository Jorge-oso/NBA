	<h2 style="font-size: 20px; text-align: justify;">El baloncesto podría ser una derivación de uno de los juegos
		más antiguos del mundo: el tlachtli o juego de la pelota. Los mayas construían en la mayoría de sus centros ceremoniales unas edificaciones de formas características para practicar este ritual. El recinto estaba compuesto por un patio central y dos transversales (uno en cada extremo del central), formando una I mayúscula. El patio central estaba flanqueado por gruesos muros inclinados y decorados, que tenían adosados los 'marcadores': anillos de piedra para hacer que una pelota de hule pasara por su centro. La pelota era durísima y lastimaba al jugador, por lo que éste llevaba protectores en los brazos y la cintura, zonas con las que debía golpear la pelota para que el juego fuera válido. Uno de los mejores recintos del juego de pelota se encuentra en Chichén Itzá. <br>
		<center><br><img src="img/historia1.jpg"></center><br> El baloncesto fue inventado en diciembre de 1891 por el clérigo, educador y físico canadiense James Naismith. Naismith introdujo el juego cuando era instructor en la Young Men's Christian Association (YMCA) en Springfield (Massachusetts), donde organizó un pasatiempo que consideraba apropiado para jugar en pista cubierta durante el invierno. El juego incluía elementos de fútbol americano, fútbol y hockey. La primera pelota usada fue de fútbol. Los equipos tenían nueve jugadores y las canastas eran de madera fijadas a los muros. Alrededor de 1897 se reglamentaron los equipos de cinco jugadores. El juego se extendió rápidamente por Estados Unidos, Canadá y otras partes del mundo, jugado tanto por hombres como por mujeres; también llegó a ser un popular deporte informal al aire libre. Los soldados estadounidenses participantes en la II Guerra Mundial popularizaron el deporte en muchos otros países. <br>
		<center><img src="img/historia2.jpg"></center><br>
